#include <unistd.h>

int		ft_strcpy(char *s1, char *s2)
{
	int i;

	i = 0;
	while (s2[i])
	{
		s1[i] = s2[i];
		i += 1;
	}
	s1[i] = s2[i];
	return (s1);
}
